debut
 ld a,1 ; met l'ecran en mode 1
 call #bc0e

 ld b,0 ; change la couleur du bord en noir (BORDER en BASIC)
 ld c,0 
 call #bc38

 ld a,1 ; change l'encre 1 en cyan (21) (INK en BASIC)
 ld b,20
 ld c,20
 call #bc32

 ld a,0 ; change l'encre 0 en noir (0) (INK en BASIC)
 ld b,0
 ld c,0
 call #bc32

 ld hl, xy ; charge l'adresse de mes coordonnées écran
boucle
 ld b, (hl) ; charge dans B le premier byte pointé par HL
 ld a, b ; charge B dans A (accumulateur) pour pouvoir faire une comparaison
 cp #01
 jr z, pause ; si A = #01, on pause et efface l'écran

 cp #00 ; si A = #00, le flag Z devient 1
 jr z, fin_boucle ; si flag Z=1, alors égalité et saut à finboucle (sortie)

 ld b, (hl)
 inc hl : ld c, (hl) ; incrémente HL pour charger le byte de poids faible de l'adresse écran
 inc hl : ld e, (hl) ; incrémente HL pour charger le bit et l'encre à allumer 
 inc hl ; passe au byte suivant

 push hl
 push bc
 push de
 ld de, 484 ; charge 484 dans DE (début de la frame précédente)
 sbc hl, de ; HL = HL - DE (HL - 484)
 ld b, (hl)
 inc hl : ld c, (hl) ; incrémente HL pour charger le byte de poids faible de l'adresse écran
 inc hl : ld e, (hl) ; incrémente HL pour charger le bit et l'encre à allumer 
 ld hl,bc
 ld (hl),#00
 
 pop de
 pop bc
 pop hl

 push hl ; sauvegarde le pointeur de données dans la pile
 ld hl, bc ; charge l'adresse écran dans HL
 ld (hl),e ; écris le byte dans la mémoire écran pointée par HL
 pop hl

 jp boucle 
pause
 call #bd19 ; synchronize avec le CRTC

 ld bc, 0x1000 ; durée de la pause dans PC
debut_pause
 dec bc ; décrémenter BC
 ld a, b ; vérifier si BC atteint 0
 or c
 jr nz, debut_pause ; continuer la boucle tant que BC ≠ 0

fin_pause
 inc hl
 jp boucle 
fin_boucle
 ld hl, xy
 call #bd19 ; synchronize avec le CRTC
 jp boucle

